package 多线程;

public class 线程概要 {

	/**
	 * 如果一次只完成一件事情，很容易实现，但事实上世间很多事情都是同时进行的。
	 * 所以在java中为了模拟这种状态，引入了线程极致。
	 * 简单的说，当程序同时完全多件事情时，就是所谓的多线程程序。
	 * 多线程应用相当广泛，使用多线程可以创建窗口程序、网络程序等。
	 * 这里介绍多线程的概念，还结合实例来了解如何使程序具有多线程功能。
	 * */
	
	
	/**
	 * 线程在windows系统中执行模式
	 * 
	 * windows操作系统是多任务操作系统，它以进程为单位。
	 * 一个进程是一个包含有自身地址的程序，每个独立执行的程序都称为进程。也就是正在执行的程序。
	 * 系统可以分配给每个进程一段 有限的 使用CPU的时间(也就是CPU时间片)
	 * CPU在这段时间中执行某个进程，然后下一个时间片又跳入另一个进程中执行。
	 * 由于CPU转换较快，所以使得每个进程好像是同时执行的一样。
	 * */
	
}
