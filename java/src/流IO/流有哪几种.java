package 流IO;

public class 流有哪几种 {

	/**
	 * Java语言定义了许多类专门负责各种方式的输入/输出，这些类都被放在java.io包中。
	 * 其中，所有输入流都是
	 * 		抽象类
	 * 			InputStream		(字节输入流)
	 * 	或
	 * 		抽象类
	 * 			Reader			(字符输入流)
	 * 的子类。
	 * 
	 * 而所有输出流都是
	 * 		抽象类
	 * 			OutputStream	(字节输出流)
	 * 	或
	 * 		抽象类
	 * 			Writer			(字符输出流)
	 * 的子类。
	 * */
}
