package 语言基础;

public class 系统内存区块 {

	/*
	 * 系统内存可 大略 分为3个区域
	 * 
	 * 即：
	 * 系统区(OS)
	 * 程序区(Program)
	 * 数据区(Data)
	 * 
	 * 当程序执行时，程序代码会加载到内存中的程序区
	 * 数据暂时存储在内存中的数据区
	 * 
	 * 
	 * */
	
	
	/**
	 * java语言中创建的对象是保存在 堆 中的。
	 * 
	 * 为了提高查找的速度而使用了散列查找，散列查找的基本思想是定义一个键来映射对象所在的内存地址。
	 * 当需要查找对象时，直接查找键就可以了，这样就不用遍历整个堆来查找对象了。
	 * 
	 * */
}
