package com.zzg.集合.demo;

/**
 * 一个学生类
 * */
public class Student {

	private String name;// 名字
	private int grade;// 班级

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

}
