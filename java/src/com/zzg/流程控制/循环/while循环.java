package com.zzg.流程控制.循环;

public class while循环 {

	public static void main(String[] args) {
		
		int i = 1;
		
		while (i < 5) {
			System.out.println("奥斯卡鲁大师考虑到开啦速度加快拉数据库达拉斯进口量达是开了");
			i++;
		}
		
		System.out.println("=================================================");
		
		int a = 1;
		do {
			System.out.println("奥斯卡鲁大师考虑到开啦速度加快拉数据库达拉斯进口量达是开了");
			a++;
		} while (a < 5);
	}
}
